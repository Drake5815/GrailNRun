    public void InsertPanel(JPanel gamePanel){
        if (this.currPanel != null){
            this.remove(this.currPanel);
        }
        if (gamePanel == LP){
            gamePanel = new LoadingPanel().endAnimation();
        }
        
        this.currPanel = gamePanel;
        this.add(gamePanel);
    }
    
